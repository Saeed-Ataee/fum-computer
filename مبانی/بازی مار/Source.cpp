int insertScore(player a[], int score, int time)
{
	int i, pos = 0;
	for(i = 4; i > -1; i --)
		if(a[i].score > score){ pos = i + 1; break;}
		if(pos < 0) pos = 0;
		if(a[pos - 1].score == score)
			if(time < a[pos - 1].time)
				pos -= 1;
		if(pos < 0) pos = 0;
		for(i = 4; i > pos; i --)
			a[i] = a[i - 1];
		return(pos);
}

